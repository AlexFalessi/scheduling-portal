package org.ow2.proactive_grid_cloud_portal.scheduler.shared;

import com.google.gwt.user.client.rpc.IsSerializable;


/**
 * This should have been a private inner class in {@link JobVisuMap}
 * but GWT won't serialize it that way..
 * 
 * 
 * @author mschnoor
 *
 */
@SuppressWarnings("serial")
class TaskPos implements IsSerializable {
    int x;

    int y;

    int w;

    int h;

    public TaskPos() {
        this(0, 0, 0, 0);
    }

    public TaskPos(int x, int y, int w, int h) {
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
    }
}
